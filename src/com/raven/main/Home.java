    private void showPerfil(){
        try {
            Usuario usuario = new Usuario();
            Connection connection = new Conexao().getConnection();
            UsuarioCRUD usuariocrud = new UsuarioCRUD(connection);

            DefaultTableModel model = (DefaultTableModel) TbPerfil.getModel();
            model.setNumRows(0);

            String email = label.getText();
            usuario.setEmail(email);
            
            ArrayList<Usuario> lista = usuariocrud.showUser(usuario);
            for (int num = 0; num < lista.size(); num++) {
            model.addRow(new Object[]{
                lista.get(num).getId(),
                lista.get(num).getNome(),
                lista.get(num).getCpf(),
                lista.get(num).getFormacao(),
                lista.get(num).getCursarea(),
                lista.get(num).getEmail(),
                lista.get(num).getSenha(),
            });
            }   
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
    
