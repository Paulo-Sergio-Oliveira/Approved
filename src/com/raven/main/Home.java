    private void jMenuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jMenuMouseClicked
        int  x = jPMenuLateral.getX();
        if (x <= -210) {
            Animacion.mover_derecha(10, 220, 15, 8, jMenu);
            Animacion.mover_derecha(-300, 0, 8, 8, jPMenuLateral);
       }else{
            Animacion.mover_izquierda(220, 10, 4, 2, jMenu);
            Animacion.mover_izquierda(0, -300, 2, 2, jPMenuLateral);
        }
        
        showPerfil();
        PNome.setVisible(true);
        PCpf.setVisible(true);
        PForm.setVisible(true);
        if("Aluno".equals((TbPerfil.getModel().getValueAt(0, 3).toString()))){
            PCurso.setVisible(true);
        } else {
            PArea.setVisible(true);
        }        
        PEmail.setVisible(true);
        PSenha.setVisible(true);
        BtEdit.setVisible(true);
        LoadPerfil();
    }//GEN-LAST:event_jMenuMouseClicked

